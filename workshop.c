#include <stdio.h>
int main()
{
    //displays the string inside the quotations
    printf("C programming \n\n");

    //integer output
    int testInteger = 5;
    printf("number = %d\n\n", testInteger);

    //double and float output
    float number1 = 13.5;
    double number2 = 12.4;
    printf("number = %f\n\n", number1);
    printf("number2 = %lf\n\n", number2);

    //print characters
    char chr ='a';
    printf("charcter = %c",chr);

    return 0;
}    

